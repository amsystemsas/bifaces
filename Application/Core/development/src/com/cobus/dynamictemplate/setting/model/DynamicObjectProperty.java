public class DynamicObjectProperty {
    private Integer idProperty;
    private Integer idDynamicObject;
    private Integer associationCount;

    public Integer getIdProperty() {
        return idProperty;
    }

    public void setIdProperty(Integer idProperty) {
        this.idProperty = idProperty;
    }

    public Integer getIdDynamicObject() {
        return idDynamicObject;
    }

    public void setIdDynamicObject(Integer idDynamicObject) {
        this.idDynamicObject = idDynamicObject;
    }

    public Integer getAssociationCount() {
        return associationCount;
    }

    public void setAssociationCount(Integer associationCount) {
        this.associationCount = associationCount;
    }
    
    
    
    

}
